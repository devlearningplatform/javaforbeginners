class Operators_Relational{
	public static void main(String param[]){
		int x = 10;
		int y = 3;
		int z = 10;
		
		System.out.print(x + " == " + y + " = ");
		System.out.println(x == y);

		System.out.print(x + " == " + z + " = ");
		System.out.println(x == z);		
		
		System.out.print(x + " != " + y + " = ");
		System.out.println(x != y);

		System.out.print(x + " < " + y + " = ");
		System.out.println(x < y);

		System.out.print(x + " < " + z + " = ");
		System.out.println(x < z);

		System.out.print(x + " > " + y + " = ");
		System.out.println(x > y);

		System.out.print(x + " > " + z + " = ");
		System.out.println(x > z);

		System.out.print(x + " <= " + y + " = ");
		System.out.println(x <= y);

		System.out.print(x + " <= " + z + " = ");
		System.out.println(x <= z);

		System.out.print(x + " >= " + y + " = ");
		System.out.println(x >= y);

		System.out.print(x + " >= " + z + " = ");
		System.out.println(x >= z);
	}
}