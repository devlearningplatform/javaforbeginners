class VariablesAndDataTypes{
	public static void main(String param[]){
		int int_var = 10;
		float float_var = 30;
		boolean bool_var = true;
		String str_var = "variable name can contain A-Z, a-z, 0-9, _ and $";
		char chr_var = '%';

		System.out.println("[1] - Value of int variable     = " + int_var);
		System.out.println("[2] - Value of float variable   = " + float_var);
		System.out.println("[3] - Value of boolean variable = " + bool_var);
		System.out.println("[4] - Value of String variable  = " + str_var);
		System.out.println("[5] - Value of char variable    = " + chr_var);
	}
}