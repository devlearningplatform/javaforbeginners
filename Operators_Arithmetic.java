class Operators_Arithmetic{
	public static void main(String param[]){
		int x = 10;
		int y = 3;
		
		int sum = x + y;
		int sub = x - y;
		int mul = x * y;
		int div = x / y;
		int mod = x % y;		

		System.out.println(x + " + " + y + " = " + sum);
		System.out.println(x + " - " + y + " = " + sub);
		System.out.println(x + " * " + y + " = " + mul);
		System.out.println(x + " / " + y + " = " + div);
		System.out.println(x + " % " + y + " = " + mod);
	}
}